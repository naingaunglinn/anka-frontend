-- ============================================================
-- win_deal() FUNCTION
-- Atomically marks a deal as won, creates a contract, and creates a project.
-- Idempotent: safe to call multiple times on the same deal.
-- contract total_value uses client_budget, then estimated_value fallback.
-- project budget_hours uses workload_hours, then sum(estimation_resources.hours) fallback.
-- ============================================================

CREATE OR REPLACE FUNCTION win_deal(p_deal_id uuid, p_tenant_id uuid)
RETURNS void
LANGUAGE plpgsql
AS $$
DECLARE
    v_deal          deals%ROWTYPE;
    v_contract_id   uuid;
    v_budget_hours  numeric;
BEGIN
    -- Lock the deal row to prevent concurrent win_deal calls
    SELECT * INTO v_deal
    FROM deals
    WHERE id = p_deal_id AND tenant_id = p_tenant_id
    FOR UPDATE;

    IF NOT FOUND THEN
        RAISE EXCEPTION 'Deal % not found for tenant %', p_deal_id, p_tenant_id;
    END IF;

    -- Idempotent: already won, nothing to do
    IF v_deal.status = 'won' THEN
        RETURN;
    END IF;

    -- Mark deal as won
    UPDATE deals
       SET status = 'won', won_at = now(), updated_at = now()
     WHERE id = p_deal_id;

    -- Prevent duplicate contract for the same deal
    IF EXISTS (
        SELECT 1 FROM contracts
        WHERE deal_id = p_deal_id AND deleted_at IS NULL
    ) THEN
        RETURN;
    END IF;

    -- Create contract (contract_number auto-generated by sequence default)
    INSERT INTO contracts (tenant_id, deal_id, client, total_value, status)
    VALUES (
        p_tenant_id,
        p_deal_id,
        COALESCE(v_deal.client, 'Client'),
        COALESCE(v_deal.client_budget, v_deal.estimated_value, 0),
        'Active'
    )
    RETURNING id INTO v_contract_id;

    -- Determine budget hours: workload_hours first, then sum estimation_resources
    SELECT COALESCE(
        v_deal.workload_hours,
        (SELECT COALESCE(SUM(hours), 0) FROM estimation_resources WHERE deal_id = p_deal_id),
        0
    ) INTO v_budget_hours;

    -- Create project (project_number auto-generated by sequence default)
    INSERT INTO projects (tenant_id, contract_id, name, client, budget_hours, status)
    VALUES (
        p_tenant_id,
        v_contract_id,
        v_deal.name,
        COALESCE(v_deal.client, 'Client'),
        v_budget_hours,
        'Not Started'
    );
END;
$$;

COMMENT ON FUNCTION win_deal(uuid, uuid) IS
'Atomically marks a deal as won and auto-generates one contract and one project in a single transaction. Idempotent - safe to call multiple times on the same deal. contract_number and project_number are auto-generated from sequences. total_value uses client_budget with estimated_value as fallback. budget_hours uses workload_hours with SUM(estimation_resources.hours) as fallback.';


