-- ============================================================
-- ANKA Agency Platform - PostgreSQL Schema
-- Target: Supabase (PostgreSQL 15+)
--
-- Rewritten from mixed-dialect original to clean PostgreSQL.
-- Changes from original:
--   - All MySQL syntax (backticks, AUTO_INCREMENT, COMMENT=) replaced
--   - Added: deal_ghost_roles, deal_hard_assignments tables
--   - Renamed: job_roles -> roles (matches frontend lib/supabaseOrganization.ts)
--   - Added: employees.role text, employees.capacity_role
--   - Added: GENERATED columns (cost_per_hour, invoices.total)
--   - Added: workload_description on deals
--   - Added: tenant_id on estimation_resources, deal_overheads, milestones, time_entries
--   - Added: ON DELETE CASCADE where child records follow parent
--   - Added: CHECK constraints on enums, ranges, non-negative values
--   - Added: win_deal() PostgreSQL function
--   - Added: sequences for contract_number, invoice_number, project_number
--   - Added: RBAC app_role on users (Admin|Executive|Sales|Delivery|HR)
--   - Removed: personal_access_tokens, password_reset_tokens (managed by Laravel)
--   - Added: seed data matching frontend mock data
--   - Added: full COMMENT ON TABLE and COMMENT ON COLUMN for every table/column
--
-- Note on company_settings.id:
--   Frontend (lib/supabaseOrganization.ts) upserts with id='singleton'.
--   For single-tenant prototypes, this works as-is.
--   For multi-tenant production, set id = tenant_id::text per tenant
--   and update the frontend/API to fetch by tenant_id.
--
-- Note on time_entries + consumed_hours:
--   Approving a time entry must increment projects.consumed_hours exactly once.
--   This is handled in application code (not a DB trigger) to avoid
--   double-counting when status toggles back and forth.
--
-- Laravel auth tables (personal_access_tokens, password_reset_tokens) are
-- intentionally excluded. Run "php artisan migrate" to generate them.
-- ============================================================


-- ============================================================
-- SEQUENCES for human-readable public IDs
-- ============================================================

CREATE SEQUENCE IF NOT EXISTS contract_number_seq START 1;
CREATE SEQUENCE IF NOT EXISTS invoice_number_seq  START 1042;
CREATE SEQUENCE IF NOT EXISTS project_number_seq  START 101;


-- ============================================================
-- TENANTS
-- Root multi-tenancy table. Every business entity belongs to a tenant.
-- ============================================================

CREATE TABLE tenants (
    id          uuid          PRIMARY KEY DEFAULT gen_random_uuid(),
    name        varchar(255)  NOT NULL,
    slug        varchar(255)  UNIQUE NOT NULL,
    plan        varchar(50)   NOT NULL DEFAULT 'free',
    is_active   boolean       NOT NULL DEFAULT true,
    created_at  timestamptz   DEFAULT now(),
    updated_at  timestamptz   DEFAULT now()
);


-- ============================================================
-- USERS
-- Auth users. Linked to a tenant and optionally to an employee record.
-- system_role: internal platform role  (owner | admin | manager | member)
-- app_role:    frontend RBAC role      (Admin | Executive | Sales | Delivery | HR)
-- employee_id FK is added after the employees table is created.
-- ============================================================

CREATE TABLE users (
    id                  bigint        PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    tenant_id           uuid          NOT NULL REFERENCES tenants(id) ON DELETE RESTRICT,
    employee_id         uuid,
    first_name          varchar(255)  NOT NULL,
    last_name           varchar(255)  NOT NULL,
    email               varchar(255)  UNIQUE NOT NULL,
    password            varchar(255)  NOT NULL,
    system_role         varchar(50)   NOT NULL DEFAULT 'member',
    app_role            text          NOT NULL DEFAULT 'Sales'
                            CHECK (app_role IN ('Admin','Executive','Sales','Delivery','HR')),
    email_verified_at   timestamptz,
    remember_token      varchar(255),
    created_at          timestamptz   DEFAULT now(),
    updated_at          timestamptz   DEFAULT now(),
    deleted_at          timestamptz
);


-- ============================================================
-- DEPARTMENTS
-- ============================================================

CREATE TABLE departments (
    id          uuid          PRIMARY KEY DEFAULT gen_random_uuid(),
    tenant_id   uuid          NOT NULL REFERENCES tenants(id) ON DELETE RESTRICT,
    name        varchar(255)  NOT NULL,
    manager     varchar(255),
    headcount   int           NOT NULL DEFAULT 0,
    created_at  timestamptz   DEFAULT now(),
    updated_at  timestamptz   DEFAULT now(),
    deleted_at  timestamptz
);


-- ============================================================
-- ROLES  (renamed from job_roles)
-- Business/billing roles with client-facing rates.
-- "department" column is a denormalized text name because the frontend
-- stores Role.department as a string, not a UUID.
-- department_id is the normalized FK for referential integrity.
-- ============================================================

CREATE TABLE roles (
    id              uuid          PRIMARY KEY DEFAULT gen_random_uuid(),
    tenant_id       uuid          NOT NULL REFERENCES tenants(id) ON DELETE RESTRICT,
    department_id   uuid          REFERENCES departments(id) ON DELETE SET NULL,
    title           varchar(255)  NOT NULL,
    department      varchar(255),
    rate            numeric(10,2) NOT NULL DEFAULT 0,
    created_at      timestamptz   DEFAULT now(),
    updated_at      timestamptz   DEFAULT now(),
    deleted_at      timestamptz
);


-- ============================================================
-- EMPLOYEES
-- "role" column stores the roles.id UUID as plain text to match the
-- frontend Employee.role string field (lib/supabaseOrganization.ts).
-- job_role_id is the normalized UUID FK for referential integrity.
-- capacity_role drives AI team builder and capacity pool calculations.
-- cost_per_hour is GENERATED: monthly_salary / workable_hours.
-- ============================================================

CREATE TABLE employees (
    id              uuid          PRIMARY KEY DEFAULT gen_random_uuid(),
    tenant_id       uuid          NOT NULL REFERENCES tenants(id) ON DELETE RESTRICT,
    department_id   uuid          REFERENCES departments(id) ON DELETE SET NULL,
    job_role_id     uuid          REFERENCES roles(id) ON DELETE SET NULL,
    name            varchar(255)  NOT NULL,
    role            text,
    role_name       varchar(255),
    capacity_role   text          CHECK (capacity_role IN ('frontend','backend','pm','qa','design')),
    monthly_salary  numeric(12,2) NOT NULL DEFAULT 0,
    workable_hours  int           NOT NULL DEFAULT 160,
    cost_per_hour   numeric(10,4) GENERATED ALWAYS AS
                        (monthly_salary / NULLIF(workable_hours, 0)) STORED,
    status          text          NOT NULL DEFAULT 'Active'
                        CHECK (status IN ('Active','On Leave','Terminated')),
    created_at      timestamptz   DEFAULT now(),
    updated_at      timestamptz   DEFAULT now(),
    deleted_at      timestamptz
);

-- Deferred FK: users.employee_id -> employees
ALTER TABLE users
    ADD CONSTRAINT fk_users_employee
    FOREIGN KEY (employee_id) REFERENCES employees(id) ON DELETE SET NULL;


-- ============================================================
-- GLOBAL OVERHEADS
-- Fixed monthly costs applied to all P&L calculations.
-- ============================================================

CREATE TABLE global_overheads (
    id            uuid          PRIMARY KEY DEFAULT gen_random_uuid(),
    tenant_id     uuid          NOT NULL REFERENCES tenants(id) ON DELETE RESTRICT,
    category      varchar(255)  NOT NULL,
    description   text,
    monthly_cost  numeric(12,2) NOT NULL DEFAULT 0,
    created_at    timestamptz   DEFAULT now(),
    updated_at    timestamptz   DEFAULT now(),
    deleted_at    timestamptz
);


-- ============================================================
-- COMPANY SETTINGS
-- One row per tenant. UNIQUE(tenant_id) enforced at DB level.
-- id='singleton' works for single-tenant prototypes (frontend default).
-- For multi-tenant: set id = tenant_id::text; fetch by tenant_id in API.
-- employer_tax_percentage and benefits_percentage support salary multiplier UI.
-- ============================================================

CREATE TABLE company_settings (
    id                      text          PRIMARY KEY DEFAULT 'singleton',
    tenant_id               uuid          UNIQUE NOT NULL REFERENCES tenants(id) ON DELETE RESTRICT,
    overhead_percentage     numeric(5,2)  NOT NULL DEFAULT 20,
    buffer_percentage       numeric(5,2)  NOT NULL DEFAULT 10,
    yearly_fixed_cost       numeric(14,2) NOT NULL DEFAULT 0,
    employer_tax_percentage numeric(5,2)  NOT NULL DEFAULT 8,
    benefits_percentage     numeric(5,2)  NOT NULL DEFAULT 12,
    created_at              timestamptz   DEFAULT now(),
    updated_at              timestamptz   DEFAULT now()
);


-- ============================================================
-- DEALS
-- CRM pipeline. Status follows Kanban stages.
-- Call win_deal(id, tenant_id) to atomically mark won and create contract+project.
-- workload_description: free-text scope description (was missing in original).
-- ============================================================

CREATE TABLE deals (
    id                      uuid          PRIMARY KEY DEFAULT gen_random_uuid(),
    tenant_id               uuid          NOT NULL REFERENCES tenants(id) ON DELETE RESTRICT,
    name                    varchar(255)  NOT NULL,
    client                  varchar(255),
    estimated_value         numeric(14,2) CHECK (estimated_value >= 0),
    win_probability         smallint      NOT NULL DEFAULT 0
                                CHECK (win_probability BETWEEN 0 AND 100),
    status                  text          NOT NULL DEFAULT 'inquiry'
                                CHECK (status IN ('lead','inquiry','opportunity','proposal','contract','won','lost')),
    client_budget           numeric(14,2) CHECK (client_budget >= 0),
    timeline_months         int,
    workload_hours          numeric(10,2) CHECK (workload_hours >= 0),
    workload_description    text,
    target_margin           numeric(5,2),
    base_labor_cost         numeric(14,2) CHECK (base_labor_cost >= 0),
    overhead_cost           numeric(14,2) CHECK (overhead_cost >= 0),
    buffer_cost             numeric(14,2) CHECK (buffer_cost >= 0),
    total_estimated_cost    numeric(14,2) CHECK (total_estimated_cost >= 0),
    estimated_gross_profit  numeric(14,2),
    won_at                  timestamptz,
    lost_at                 timestamptz,
    created_at              timestamptz   DEFAULT now(),
    updated_at              timestamptz   DEFAULT now(),
    deleted_at              timestamptz
);


-- ============================================================
-- ESTIMATION RESOURCES
-- Feature-level resource breakdown per deal.
-- role_id (text) mirrors frontend EstimationResource.roleId string.
-- job_role_id (uuid) is the normalized FK for referential integrity.
-- Replaced wholesale on deal estimation update.
-- ============================================================

CREATE TABLE estimation_resources (
    id            uuid          PRIMARY KEY DEFAULT gen_random_uuid(),
    tenant_id     uuid          NOT NULL REFERENCES tenants(id) ON DELETE RESTRICT,
    deal_id       uuid          NOT NULL REFERENCES deals(id) ON DELETE CASCADE,
    job_role_id   uuid          REFERENCES roles(id) ON DELETE SET NULL,
    role_id       text,
    feature_name  varchar(255)  NOT NULL,
    hours         numeric(10,2) NOT NULL DEFAULT 0 CHECK (hours >= 0),
    created_at    timestamptz   DEFAULT now(),
    updated_at    timestamptz   DEFAULT now()
);


-- ============================================================
-- DEAL OVERHEADS  (maps to frontend ProjectOverhead)
-- Per-deal overhead items, separate from global_overheads.
-- ============================================================

CREATE TABLE deal_overheads (
    id          uuid          PRIMARY KEY DEFAULT gen_random_uuid(),
    tenant_id   uuid          NOT NULL REFERENCES tenants(id) ON DELETE RESTRICT,
    deal_id     uuid          NOT NULL REFERENCES deals(id) ON DELETE CASCADE,
    name        varchar(255)  NOT NULL,
    cost        numeric(12,2) NOT NULL DEFAULT 0 CHECK (cost >= 0),
    created_at  timestamptz   DEFAULT now(),
    updated_at  timestamptz   DEFAULT now()
);


-- ============================================================
-- DEAL GHOST ROLES  (maps to frontend Deal.ghostRoles)
-- Unassigned role slots used for soft capacity planning before
-- named engineers are assigned. Soft-booked hours are derived from
-- quantity * months * 160 * (win_probability / 100).
-- ============================================================

CREATE TABLE deal_ghost_roles (
    id                  uuid          PRIMARY KEY DEFAULT gen_random_uuid(),
    tenant_id           uuid          NOT NULL REFERENCES tenants(id) ON DELETE RESTRICT,
    deal_id             uuid          NOT NULL REFERENCES deals(id) ON DELETE CASCADE,
    role_type           text          NOT NULL
                            CHECK (role_type IN ('frontend','backend','pm','qa','design')),
    quantity            int           NOT NULL DEFAULT 1 CHECK (quantity >= 1),
    months              int           NOT NULL DEFAULT 1 CHECK (months >= 1),
    avg_monthly_salary  numeric(12,2) NOT NULL DEFAULT 0 CHECK (avg_monthly_salary >= 0),
    created_at          timestamptz   DEFAULT now(),
    updated_at          timestamptz   DEFAULT now()
);


-- ============================================================
-- DEAL HARD ASSIGNMENTS  (maps to frontend Deal.hardAssignments)
-- Named engineers committed to a deal (won/contract stage).
-- Required for AI team acceptance and staffing pages.
-- UNIQUE(deal_id, employee_id) prevents duplicate assignments.
-- ============================================================

CREATE TABLE deal_hard_assignments (
    id              uuid          PRIMARY KEY DEFAULT gen_random_uuid(),
    tenant_id       uuid          NOT NULL REFERENCES tenants(id) ON DELETE RESTRICT,
    deal_id         uuid          NOT NULL REFERENCES deals(id) ON DELETE CASCADE,
    employee_id     uuid          NOT NULL REFERENCES employees(id) ON DELETE CASCADE,
    allocated_hours numeric(10,2) NOT NULL DEFAULT 0 CHECK (allocated_hours >= 0),
    created_at      timestamptz   DEFAULT now(),
    updated_at      timestamptz   DEFAULT now(),
    UNIQUE (deal_id, employee_id)
);


-- ============================================================
-- CONTRACTS
-- Auto-generated by win_deal(). Can also be created manually.
-- contract_number uses contract_number_seq (e.g. CON-0001).
-- ============================================================

CREATE TABLE contracts (
    id                  uuid          PRIMARY KEY DEFAULT gen_random_uuid(),
    tenant_id           uuid          NOT NULL REFERENCES tenants(id) ON DELETE RESTRICT,
    deal_id             uuid          REFERENCES deals(id) ON DELETE SET NULL,
    contract_number     text          UNIQUE NOT NULL DEFAULT
                            ('CON-' || lpad(nextval('contract_number_seq')::text, 4, '0')),
    client              varchar(255)  NOT NULL,
    total_value         numeric(14,2) NOT NULL DEFAULT 0,
    revenue_recognized  numeric(14,2) NOT NULL DEFAULT 0,
    status              text          NOT NULL DEFAULT 'Draft'
                            CHECK (status IN ('Draft','Active','Completed','Cancelled')),
    start_date          date,
    end_date            date,
    notes               text,
    created_at          timestamptz   DEFAULT now(),
    updated_at          timestamptz   DEFAULT now(),
    deleted_at          timestamptz
);


-- ============================================================
-- MILESTONES
-- Payment milestones tied to a contract.
-- ============================================================

CREATE TABLE milestones (
    id            uuid          PRIMARY KEY DEFAULT gen_random_uuid(),
    tenant_id     uuid          NOT NULL REFERENCES tenants(id) ON DELETE RESTRICT,
    contract_id   uuid          NOT NULL REFERENCES contracts(id) ON DELETE CASCADE,
    name          varchar(255)  NOT NULL,
    due_date      date          NOT NULL,
    amount        numeric(12,2) NOT NULL DEFAULT 0 CHECK (amount >= 0),
    status        text          NOT NULL DEFAULT 'Pending'
                      CHECK (status IN ('Pending','In Progress','Completed')),
    completed_at  timestamptz,
    created_at    timestamptz   DEFAULT now(),
    updated_at    timestamptz   DEFAULT now()
);


-- ============================================================
-- INVOICES
-- total is GENERATED as amount + tax (no application math needed).
-- issue_date is the primary date column; frontend Invoice.date maps here.
-- invoice_number uses invoice_number_seq (e.g. INV-1042).
-- ============================================================

CREATE TABLE invoices (
    id              uuid          PRIMARY KEY DEFAULT gen_random_uuid(),
    tenant_id       uuid          NOT NULL REFERENCES tenants(id) ON DELETE RESTRICT,
    contract_id     uuid          NOT NULL REFERENCES contracts(id) ON DELETE RESTRICT,
    milestone_id    uuid          REFERENCES milestones(id) ON DELETE SET NULL,
    invoice_number  text          UNIQUE NOT NULL DEFAULT
                        ('INV-' || lpad(nextval('invoice_number_seq')::text, 4, '0')),
    issue_date      date          NOT NULL,
    due_date        date,
    amount          numeric(12,2) NOT NULL DEFAULT 0 CHECK (amount >= 0),
    tax             numeric(10,2) NOT NULL DEFAULT 0 CHECK (tax >= 0),
    total           numeric(12,2) GENERATED ALWAYS AS (amount + tax) STORED,
    status          text          NOT NULL DEFAULT 'Draft'
                        CHECK (status IN ('Draft','Pending','Paid','Overdue','Cancelled')),
    paid_at         timestamptz,
    notes           text,
    created_at      timestamptz   DEFAULT now(),
    updated_at      timestamptz   DEFAULT now(),
    deleted_at      timestamptz
);


-- ============================================================
-- PROJECTS
-- Auto-created by win_deal(). UNIQUE(contract_id) enforces one project
-- per contract, matching the frontend winDeal() behavior.
-- consumed_hours is updated by application code on time entry approval.
-- project_number uses project_number_seq (e.g. PRJ-101).
-- ============================================================

CREATE TABLE projects (
    id              uuid          PRIMARY KEY DEFAULT gen_random_uuid(),
    tenant_id       uuid          NOT NULL REFERENCES tenants(id) ON DELETE RESTRICT,
    contract_id     uuid          NOT NULL REFERENCES contracts(id) ON DELETE RESTRICT,
    project_number  text          UNIQUE NOT NULL DEFAULT
                        ('PRJ-' || lpad(nextval('project_number_seq')::text, 3, '0')),
    name            varchar(255)  NOT NULL,
    client          varchar(255)  NOT NULL,
    budget_hours    numeric(10,2) NOT NULL DEFAULT 0 CHECK (budget_hours >= 0),
    consumed_hours  numeric(10,2) NOT NULL DEFAULT 0 CHECK (consumed_hours >= 0),
    status          text          NOT NULL DEFAULT 'Not Started'
                        CHECK (status IN ('Not Started','On Track','At Risk','Over Budget','Completed')),
    start_date      date,
    end_date        date,
    created_at      timestamptz   DEFAULT now(),
    updated_at      timestamptz   DEFAULT now(),
    deleted_at      timestamptz,
    UNIQUE (contract_id)
);


-- ============================================================
-- TIME ENTRIES
-- hours > 0 enforced (cannot log zero or negative hours).
-- Approving a time entry must increment projects.consumed_hours in
-- application code, not via trigger, to avoid double-counting when
-- status changes repeatedly (Draft -> Pending -> Approved -> Pending).
-- ============================================================

CREATE TABLE time_entries (
    id            uuid          PRIMARY KEY DEFAULT gen_random_uuid(),
    tenant_id     uuid          NOT NULL REFERENCES tenants(id) ON DELETE RESTRICT,
    project_id    uuid          NOT NULL REFERENCES projects(id) ON DELETE RESTRICT,
    employee_id   uuid          NOT NULL REFERENCES employees(id) ON DELETE RESTRICT,
    approved_by   bigint        REFERENCES users(id) ON DELETE SET NULL,
    task          varchar(255)  NOT NULL,
    date          date          NOT NULL,
    hours         numeric(6,2)  NOT NULL CHECK (hours > 0),
    billable      boolean       NOT NULL DEFAULT true,
    status        text          NOT NULL DEFAULT 'Draft'
                      CHECK (status IN ('Draft','Pending','Approved','Rejected')),
    notes         text,
    approved_at   timestamptz,
    created_at    timestamptz   DEFAULT now(),
    updated_at    timestamptz   DEFAULT now()
);

