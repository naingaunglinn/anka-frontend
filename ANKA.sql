-- ============================================================
-- TABLE COMMENTS
-- Visible in DB diagram tools (dbdiagram.io, DBeaver, TablePlus, etc.)
-- ============================================================

COMMENT ON TABLE tenants IS
'Root multi-tenancy table. Every other table references tenant_id to isolate data per company. A tenant represents one agency/business using the ANKA platform.';

COMMENT ON TABLE users IS
'Authentication accounts for people who log in. Each user belongs to one tenant and optionally links to an employee record. Two role columns exist: system_role for platform admin levels (owner/admin/manager/member), app_role for frontend RBAC features (which pages and actions are accessible).';

COMMENT ON TABLE departments IS
'Organizational units within a tenant, e.g. Engineering, Design, Marketing. Used to group employees and roles. headcount is maintained by application code when employees are added or removed from a department.';

COMMENT ON TABLE roles IS
'Billable job roles with client-facing hourly rates. e.g. Senior Developer at $150/hr. Renamed from job_roles to match the frontend Supabase table expectation (lib/supabaseOrganization.ts). The department text column is denormalized (stores the name as a string) because the frontend Role type sends a plain string rather than a UUID.';

COMMENT ON TABLE employees IS
'The organizational roster of delivery staff. Each employee has a monthly salary, workable hours per month, and a capacity_role that classifies them for the AI team builder and capacity pool. cost_per_hour is automatically calculated by the database from monthly_salary / workable_hours and never needs to be set manually.';

COMMENT ON TABLE global_overheads IS
'Company-wide fixed monthly costs such as rent, software subscriptions, and utilities. Summed each month and applied to the P&L dashboard. Distinct from per-deal overheads stored in deal_overheads.';

COMMENT ON TABLE company_settings IS
'One configuration row per tenant holding the financial percentages used in deal estimation (overhead %, buffer %) and P&L calculations. The id defaults to "singleton" for single-tenant prototypes to match the frontend upsert call. For multi-tenant production, use id = tenant_id::text so each tenant has a distinct row.';

COMMENT ON TABLE deals IS
'The CRM sales pipeline. A deal starts as a lead or inquiry and progresses through Kanban stages (opportunity, proposal, contract) to won or lost. All estimation math (labor cost, overhead, buffer, gross profit) is computed on the frontend and stored here. Call win_deal(id, tenant_id) to atomically mark a deal as won and auto-create a contract and project.';

COMMENT ON TABLE estimation_resources IS
'Line items that break a deal workload into named features and the role/hours required. Example: "Architecture Setup" needs 100 hours of a Senior Developer. These rows are deleted and replaced as a complete set whenever a deal estimation is updated. role_id is a text copy of roles.id because the frontend sends it as a string.';

COMMENT ON TABLE deal_overheads IS
'Deal-specific one-time costs such as "AWS Setup Fee $2,000" or "Travel Expenses $500". Distinct from global_overheads which apply company-wide. Maps to the frontend ProjectOverhead TypeScript type. Rows are cascade-deleted when the deal is removed.';

COMMENT ON TABLE deal_ghost_roles IS
'Unnamed role slots on a deal used for soft capacity planning before real engineers are assigned. Example: "2 backend engineers for 3 months at $7,000/month average". Soft-booked hours = quantity x months x 160hrs x (win_probability/100). Cascade-deleted with the deal.';

COMMENT ON TABLE deal_hard_assignments IS
'Actual named engineer assignments on a deal once it reaches the won or contract stage. Maps to frontend Deal.hardAssignments. A UNIQUE constraint on (deal_id, employee_id) prevents assigning the same person twice. Required by the AI team builder acceptance flow and the staffing capacity view.';

COMMENT ON TABLE contracts IS
'Formal client agreements generated when a deal is won. contract_number (e.g. CON-0001) is auto-generated from a sequence. One contract is created per won deal but contracts can also be created manually. revenue_recognized tracks how much of total_value has been invoiced and paid so far.';

COMMENT ON TABLE milestones IS
'Payment checkpoints within a contract. Example: "Project Kickoff - $20,000 due 2024-03-15". Milestones split the contract total into scheduled billing events. Each can optionally trigger an invoice. Cascade-deleted when the contract is removed.';

COMMENT ON TABLE invoices IS
'Client-facing billing documents tied to a contract. invoice_number (e.g. INV-1042) is auto-generated. The total column is automatically calculated by the database as amount + tax - the application never needs to compute it. issue_date is what the frontend TypeScript type calls "date".';

COMMENT ON TABLE projects IS
'The delivery project created when a deal is won. project_number (e.g. PRJ-101) is auto-generated. A UNIQUE constraint on contract_id enforces one project per contract, matching the frontend win-deal behavior. consumed_hours is incremented by the application each time a time entry is approved.';

COMMENT ON TABLE time_entries IS
'Individual timesheet records logged by employees against a project. hours must be greater than zero. The approval workflow is Draft -> Pending -> Approved (or Rejected). Only Approved entries should increment projects.consumed_hours, and the application must ensure this happens exactly once per approval to avoid double-counting.';


