export type DealWizardStep = 'context' | 'estimation' | 'staffing' | 'complete';

export type DealLeadSource =
    | 'inbound'
    | 'referral'
    | 'cold_outreach'
    | 'social'
    | 'event'
    | 'partner'
    | 'other';

export interface Deal {
    id: string;
    name: string;
    client?: string;
    // Primary contact at the client company
    contactName?: string;
    contactEmail?: string;
    contactPhone?: string;
    estimatedValue?: number;
    winProbability?: number;
    // 5-stage pipeline. UI rank labels (in lib/dealRanks.ts):
    //   lead → C, qualified → B, negotiation → A, won → S, lost → D.
    // The old "proposal" stage was merged into "qualified" in migration
    // 2026_05_12_000001_collapse_proposal_into_qualified.php.
    status?: "lead" | "qualified" | "negotiation" | "won" | "lost";
    wizardStep?: DealWizardStep;
    expectedCloseDate?: string;   // YYYY-MM-DD
    leadSource?: DealLeadSource;

    // Legacy fields for Deals UI
    clientBudget?: number;
    timelineMonths?: number;
    workloadHours?: number;
    workloadDescription?: string;
    ghostRoles?: GhostRole[];
    hardAssignments?: HardAssignment[];
    baseLaborCost?: number;
    overheadCost?: number;
    bufferCost?: number;
    totalEstimatedCost?: number;
    estimatedGrossProfit?: number;

    // Estimation Data attached to Deal
    estimationResources?: EstimationResource[];
    projectOverheads?: ProjectOverhead[];
    targetMargin?: number;

    // Closure metadata
    winReason?: string;
    lossReason?: string;
}

