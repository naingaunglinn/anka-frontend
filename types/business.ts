// --- Organization ---
export interface Role {
    id: string;
    title: string;
    department: string;      // denormalized text — fast reads
    departmentId?: string;   // FK → departments.id
    rate: number;            // Billable rate to client
}

export interface Department {
    id: string;
    name: string;
    managerId?: string;    // FK → employees.id
    managerName?: string;  // denormalized display name (eager-loaded server-side)
    headcount: number;     // computed server-side as employees_count
}

export interface Employee {
    id: string;
    name: string;
    role: string;           // job_role_id UUID as text (billing role FK)
    roleName?: string;      // denormalized display name (role_name column)
    departmentId?: string;  // FK → departments.id
    departmentName?: string; // denormalized display name (eager-loaded server-side)
    jobRoleId?: string;     // FK → roles.id (proper relational FK)
    capacityRole?: RoleType; // capacity pool bucket (capacity_role column, text)
    capacityRoleId?: string; // FK → capacity_roles.id (NEW)
    capacityRoleName?: string; // denormalized display from capacity_roles (NEW)
    // Seniority rank — added in chg-009. Used by the AI Team Builder to make
    // seniority decisions deterministic instead of keyword-matching free-text
    // titles. Defaults seeded as Junior/Mid/Senior/Lead per tenant; tenants
    // can add custom ranks via the Organization → Ranks tab.
    rankId?: string | null;
    rank?: Rank | null;
    monthlySalary: number;
    workableHours: number;
    costPerHour: number;    // READ-ONLY: GENERATED by DB (monthly_salary / workable_hours)
    status: 'Active' | 'On Leave' | 'Terminated';
    email?: string;         // pulled from the linked users row; absent if no login account exists
    skills?: EmployeeSkillWithName[]; // employee's skills (NEW)
}

/**
 * Tenant-managed seniority rank. Higher `level` = more senior. The four
 * seeded defaults are Junior(10), Mid(20), Senior(30), Lead(40). Tenants
 * can add custom ranks ("Principal", "Staff Engineer") — levels are
 * spaced with gaps to make insertion easy.
 */
export interface Rank {
    id: string;
    name: string;
    code: string;
    level: number;
}

